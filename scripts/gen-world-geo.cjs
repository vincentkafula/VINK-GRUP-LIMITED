// One-off generator: converts real Natural Earth coastline data (via the
// world-atlas package) into the [lat, lng] ring format WorldMapSection's
// canvas globe expects. Run with: node scripts/gen-world-geo.cjs
//
// This replaces the old hand-drawn, eyeballed continent shapes with actual
// geographic data, and assigns climate-band colors by each ring's centroid
// latitude (tropical / subtropical / temperate / tundra / ice) instead of
// the old hardcoded per-continent "desert"/"savanna" guesses.

const topojson = require("topojson-client");
const land = require("world-atlas/land-110m.json");
const countries = require("world-atlas/countries-110m.json");

function ringToLatLng(ring) {
  // GeoJSON is [lng, lat]; our renderer wants [lat, lng]. Round to 2dp —
  // plenty of precision for a 520px canvas globe, keeps the data compact.
  return ring.map(([lng, lat]) => [
    Math.round(lat * 100) / 100,
    Math.round(lng * 100) / 100,
  ]);
}

function biomeForLat(avgLat) {
  const a = Math.abs(avgLat);
  if (a >= 70) return "ice";
  if (a >= 60) return "tundra";
  if (a >= 35) return "forest";   // temperate
  if (a >= 15) return "savanna";  // subtropical
  return "tropical";
}

function extractRings(geometry) {
  // geometry.coordinates for MultiPolygon: Polygon[][], for Polygon: ring[]
  const polys = geometry.type === "MultiPolygon" ? geometry.coordinates : [geometry.coordinates];
  const rings = [];
  for (const poly of polys) {
    for (const ring of poly) {
      if (ring.length < 4) continue; // degenerate
      rings.push(ring);
    }
  }
  return rings;
}

// ── General landmasses (no country subdivisions, clean coastlines) ─────────
const landGeo = topojson.feature(land, land.objects.land);
const landRings = extractRings(landGeo.features[0].geometry);

const entries = landRings.map((ring) => {
  const pts = ringToLatLng(ring);
  const avgLat = pts.reduce((s, [la]) => s + la, 0) / pts.length;
  return { biome: biomeForLat(avgLat), pts };
});

// ── South Africa highlight (drawn on top in the accent color) ──────────────
const countriesGeo = topojson.feature(countries, countries.objects.countries);
const sa = countriesGeo.features.find((f) => f.properties && f.properties.name === "South Africa");
if (!sa) throw new Error("South Africa not found in countries-110m dataset");
const saRings = extractRings(sa.geometry);
saRings.forEach((ring) => {
  entries.push({ biome: "sa", pts: ringToLatLng(ring) });
});

// ── Emit TS module ──────────────────────────────────────────────────────────
const header = `// AUTO-GENERATED by scripts/gen-world-geo.cjs — do not hand-edit.
// Real-world coastline data derived from Natural Earth (110m resolution) via
// the "world-atlas" package: https://github.com/topojson/world-atlas
// Regenerate with: node scripts/gen-world-geo.cjs

export type LandPoly = { pts: [number, number][]; biome?: "forest" | "desert" | "savanna" | "tundra" | "ice" | "sa" | "tropical" };

export const LAND: LandPoly[] = [
`;

const body = entries
  .map((e) => {
    const pts = e.pts.map(([la, ln]) => `[${la},${ln}]`).join(",");
    return `  { biome: "${e.biome}", pts: [${pts}] },`;
  })
  .join("\n");

const footer = `\n];\n`;

require("fs").writeFileSync(
  require("path").join(__dirname, "..", "src", "app", "components", "worldMapGeoData.ts"),
  header + body + footer
);

console.log(`Wrote ${entries.length} rings (${landRings.length} land + ${saRings.length} SA) to src/app/components/worldMapGeoData.ts`);
